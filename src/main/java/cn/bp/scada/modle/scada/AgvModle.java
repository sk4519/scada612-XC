package cn.bp.scada.modle.scada;

import java.math.BigDecimal;

public class AgvModle {
	public BigDecimal whRow;
	public BigDecimal whId;
	public String whName;
	public BigDecimal whColumn;
	public String whType;
	public String whFlag;

	public BigDecimal getWhRow() {
		return whRow;
	}

	public void setWhRow(BigDecimal whRow) {
		this.whRow = whRow;
	}

	public BigDecimal getWhId() {
		return whId;
	}

	public void setWhId(BigDecimal whId) {
		this.whId = whId;
	}

	public String getWhName() {
		return whName;
	}

	public void setWhName(String whName) {
		this.whName = whName;
	}

	public BigDecimal getWhColumn() {
		return whColumn;
	}

	public void setWhColumn(BigDecimal whColumn) {
		this.whColumn = whColumn;
	}

	public String getWhType() {
		return whType;
	}

	public void setWhType(String whType) {
		this.whType = whType;
	}

	public String getWhFlag() {
		return whFlag;
	}

	public void setWhFlag(String whFlag) {
		this.whFlag = whFlag;
	}

}
